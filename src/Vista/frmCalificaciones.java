    private void btnPromedioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPromedioActionPerformed
        // TODO add your handling code here
        int i=0;
        
        double promEsp, promMat, promFomr, promArt, promNat, promMedio, promFis;

        promEsp = (Double.parseDouble(txtEsp.getText()) + Double.parseDouble(txtEsp2.getText()) + Double.parseDouble(txtEsp3.getText()) + Double.parseDouble(txtEsp4.getText()))/4;
        txtPromEsp.setText(String.valueOf(promEsp));
        
        promMat = (Double.parseDouble(txtMat.getText()) + Double.parseDouble(txtMat2.getText()) + Double.parseDouble(txtMat3.getText())+ Double.parseDouble(txtMat4.getText()))/4;
        txtPromMat.setText(String.valueOf(promMat));
        
        promFomr = (Double.parseDouble(txtForm.getText()) + Double.parseDouble(txtForm2.getText()) + Double.parseDouble(txtForm3.getText()) + Double.parseDouble(txtForm4.getText()))/4;
        txtPromForm.setText(String.valueOf(promFomr));
        
        promNat = (Double.parseDouble(txtNat.getText()) + Double.parseDouble(txtNat2.getText()) + Double.parseDouble(txtNat3.getText()) + Double.parseDouble(txtNat4.getText()))/4;
        txtPromNat.setText(String.valueOf(promNat));
        
        promArt = (Double.parseDouble(txtArt.getText()) + Double.parseDouble(txtArt2.getText()) + Double.parseDouble(txtArt3.getText()) + Double.parseDouble(txtArt4.getText()))/4;
        txtPromArt.setText(String.valueOf(promArt));
        
        promMedio = (Double.parseDouble(txtMedio.getText()) + Double.parseDouble(txtMedio2.getText()) + Double.parseDouble(txtMedio3.getText()) + Double.parseDouble(txtMedio4.getText()))/4;
        txtPromMedio.setText(String.valueOf(promMedio));
        
        promFis= (Double.parseDouble(txtFis.getText()) + Double.parseDouble(txtFis2.getText()) + Double.parseDouble(txtFis3.getText()) + Double.parseDouble(txtFis4.getText()))/4;
        txtPromFis.setText(String.valueOf(promFis));
        
        if(promEsp<60)
            i++;
        if(promMat<60)
            i++;
        if(promFomr<60)
            i++;
        if(promNat<60)
            i++;
        if(promArt<60)
            i++;
        if(promMedio<60)
            i++;
        if(promFis<60)
            i++;
        
        if(i>0)
            JOptionPane.showMessageDialog(null,"El alumno reprobo " + i + " materias","Informacion", JOptionPane.INFORMATION_MESSAGE);
        
    }//GEN-LAST:event_btnPromedioActionPerformed

